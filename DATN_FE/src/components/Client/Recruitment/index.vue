<script>
export default {
  data() {
    return {
      activeTab: "education",
      observer: null,
    };
  },
  mounted() {
    this.initScrollAnimation();
  },
  beforeDestroy() {
    if (this.observer) {
      this.observer.disconnect();
    }
  },
  methods: {
    initScrollAnimation() {
      // Tạo Intersection Observer để theo dõi khi element xuất hiện
      this.observer = new IntersectionObserver(
        (entries) => {
          entries.forEach((entry) => {
            if (entry.isIntersecting) {
              const element = entry.target;
              const animation = element.dataset.animation;
              const delay = element.dataset.delay || 0;
              
              // Thêm delay nếu có
              setTimeout(() => {
                element.classList.add('animate-in');
              }, delay);
              
              // Ngừng observe element này sau khi đã animate
              this.observer.unobserve(element);
            }
          });
        },
        {
          threshold: 0.1, // Trigger khi 10% element xuất hiện
          rootMargin: '0px 0px -50px 0px' // Trigger sớm hơn 50px
        }
      );

      // Observe tất cả elements có class scroll-animate
      const animateElements = this.$el.querySelectorAll('.scroll-animate');
      animateElements.forEach((el) => {
        this.observer.observe(el);
      });
    }
  }
};
</script>

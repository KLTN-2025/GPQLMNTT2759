<script>
export default {
  name: 'BigProgram',
  mounted() {
    this.initScrollReveal();
  },
  beforeUnmount() {
    // Cleanup observer
    if (this.observer) {
      this.observer.disconnect();
    }
  },
  methods: {
    initScrollReveal() {
      // Tạo Intersection Observer
      this.observer = new IntersectionObserver(
        (entries) => {
          entries.forEach((entry) => {
            if (entry.isIntersecting) {
              const delay = entry.target.dataset.delay || 0;
              
              setTimeout(() => {
                entry.target.classList.add('revealed');
              }, parseInt(delay));
              
              // Unobserve element sau khi đã reveal
              this.observer.unobserve(entry.target);
            }
          });
        },
        {
          threshold: 0.1, // Trigger khi 10% element xuất hiện
          rootMargin: '0px 0px -50px 0px' // Trigger sớm hơn 50px
        }
      );

      // Observe tất cả elements có class scroll-reveal
      this.$nextTick(() => {
        const elements = this.$el.querySelectorAll('.scroll-reveal');
        elements.forEach((el) => {
          this.observer.observe(el);
        });
      });
    }
  }
};
</script>
